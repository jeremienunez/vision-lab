export const requiredP0BootstrapPaths = [
  'api/Cargo.toml',
  'api/crates/perception_domain/Cargo.toml',
  'api/crates/perception_domain/src/lib.rs',
  'api/crates/perception_app/Cargo.toml',
  'api/crates/perception_app/src/lib.rs',
  'api/crates/perception_infra/Cargo.toml',
  'api/crates/perception_infra/src/lib.rs',
  'api/crates/perception_http/Cargo.toml',
  'api/crates/perception_http/src/lib.rs',
  'api/crates/perception_api/Cargo.toml',
  'api/crates/perception_api/src/main.rs',
  'worker/pyproject.toml',
  'worker/perception_worker/__init__.py',
  'worker/tests/test_worker_package.py',
  '.env.example',
  'scripts/bootstrap-local-env.mjs',
];

function normalizePath(path) {
  return String(path ?? '').replaceAll('\\', '/').replace(/^\.\//, '').replace(/\/+$/, '');
}

function normalizeProjectRoot(projectRoot) {
  const normalized = normalizePath(projectRoot);

  if (!normalized.startsWith('/')) {
    throw new Error(`Project root must be absolute: ${projectRoot}`);
  }

  return normalized;
}

export function validateP0BootstrapPaths(paths) {
  const pathSet = new Set(paths.map(normalizePath).filter(Boolean));
  const errors = [];

  for (const requiredPath of requiredP0BootstrapPaths) {
    if (!pathSet.has(requiredPath)) {
      errors.push(`Missing required P0 bootstrap path: ${requiredPath}.`);
    }
  }

  return {
    valid: errors.length === 0,
    errors,
  };
}

export function buildLocalEnvContent(projectRoot) {
  const root = normalizeProjectRoot(projectRoot);

  return [
    '# Generated by npm run bootstrap:env',
    'PERCEPTIONLAB_ENV=local',
    `PERCEPTIONLAB_PROJECT_ROOT=${root}`,
    `PERCEPTIONLAB_DATA_ROOT=${root}/datasets`,
    `PERCEPTIONLAB_SEED_DATASET_ROOT=${root}/datasets/seed`,
    `PERCEPTIONLAB_STORAGE_ROOT=${root}/.perceptionlab/storage`,
    `PERCEPTIONLAB_ARTIFACT_ROOT=${root}/.perceptionlab/artifacts`,
    `PERCEPTIONLAB_TMP_ROOT=${root}/.perceptionlab/tmp`,
    'PERCEPTIONLAB_DATABASE_URL=postgres://perceptionlab:perceptionlab@localhost:5432/perceptionlab',
    'PERCEPTIONLAB_QUEUE_URL=postgres://perceptionlab:perceptionlab@localhost:5432/perceptionlab',
    'PERCEPTIONLAB_OBJECT_STORAGE_ENDPOINT=http://localhost:9000',
    'PERCEPTIONLAB_OBJECT_STORAGE_BUCKET=perceptionlab',
    '',
  ].join('\n');
}
